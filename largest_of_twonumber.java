import java.util.*;
public class largest_of_twonumber {
    public static void main(String args[]){
            Scanner sc=new Scanner(System.in);
            int a=sc.nextInt();
            int b=sc.nextInt();
            if(a>b){
                System.out.print("a is bigger");
            }
            else{
                System.out.print("b is bigger");
            }
    }
    
}
