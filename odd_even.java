import java.util.*;
public class odd_even {
    public static void main(String args[]){
            Scanner sc=new Scanner(System.in);
            int a=sc.nextInt();
            
            if(a%2==0){
                System.out.print("a is even");
            }
            else{
                System.out.print("a is odd");
            }
    }
    
}
